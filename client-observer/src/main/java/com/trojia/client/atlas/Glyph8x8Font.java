    private Glyph8x8Font() {
    }

    /**
     * One row of a glyph's bitmap.
     *
     * @param glyph the character; anything outside {@code 0x20..0x7E} substitutes
     *              the hollow-box fallback
     * @param row   row index 0 (top) .. 7 (bottom)
     * @return the row bits, 0..255; bit 7 ({@code 0x80}) is the leftmost pixel
     * @throws IllegalArgumentException if {@code row} is outside 0..7
     */
    public static int rowBits(char glyph, int row) {
        if (row < 0 || row >= HEIGHT) {
            throw new IllegalArgumentException("row " + row + " outside 0.." + (HEIGHT - 1));
        }
        if (glyph < FIRST || glyph > LAST) {
            return FALLBACK_ROWS[row];
        }
        return ROWS[(glyph - FIRST) * HEIGHT + row];
    }

    /**
     * Whether one pixel of a glyph is inked.
     *
     * @param glyph the character (fallback substitution as in {@link #rowBits})
     * @param x     column 0 (left) .. 7 (right)
     * @param y     row 0 (top) .. 7 (bottom)
     * @return true if the pixel is set
     * @throws IllegalArgumentException if {@code x} or {@code y} is outside 0..7
     */
    public static boolean isInk(char glyph, int x, int y) {
        if (x < 0 || x >= WIDTH) {
            throw new IllegalArgumentException("x " + x + " outside 0.." + (WIDTH - 1));
        }
        return (rowBits(glyph, y) & (0x80 >> x)) != 0;
    }
